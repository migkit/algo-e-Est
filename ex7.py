num1 = float (input ( "insira um número: ")) 
num2 = float (input ( "insira um número: ")) 
ope = (input ( "insira uma operação(+,*,/,-): ")) 

if ope == "+": 
    print (num1 + num2)
elif ope == "-": 
    print (num1 - num2) 
elif ope == "*":
    print (num1 * num2)
else: 
    print (num1 / num2) 

